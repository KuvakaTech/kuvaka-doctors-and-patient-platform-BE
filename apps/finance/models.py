"""
Doctor income attribution — "what did this doctor earn, from where".

This app never imports apps.billing at module level — the bridge
(apps.finance.services.record_billing_payment, called by apps.billing.services
via a function-local import) is the only thing that touches ChargeItem,
and only ever by primary key / FK assignment, never a model import.
"""

from decimal import Decimal

from django.conf import settings
from django.db import models
from django.utils import timezone

from apps.core.models import BaseModel
from apps.core.money import DEFAULT_CURRENCY, PaymentMode, quantize2

# ---------------------------------------------------------------------------
# BusinessUnit — what earned the money
# ---------------------------------------------------------------------------


class BusinessUnitType(models.TextChoices):
    CLINIC = "clinic", "Clinic"
    LAB = "lab", "Lab"
    TEST_CENTER = "test_center", "Test Center"
    PHARMACY = "pharmacy", "Pharmacy / Medical Store"
    DEVICE = "device", "Device"
    OTHER = "other", "Other"


class BusinessOwnership(models.TextChoices):
    OWNED = "owned", "Owned"
    PARTNERED = "partnered", "Partnered"


class BusinessUnit(BaseModel):
    """
    One income source in a doctor's practice — a clinic, lab, pharmacy,
    test center, device, or partnership. `RevenueEntry` rows attribute
    income to one of these (or to none, for personal income like home
    visits and MR honoraria that don't belong to a facility).

    A platform `Clinic` gets a matching unit auto-created on registration
    (see apps.clinics.views.ClinicViewSet.perform_create) and via a
    backfill migration for clinics that existed before this app did.
    """

    owner = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.CASCADE, related_name="business_units"
    )
    name = models.CharField(max_length=255)
    unit_type = models.CharField(max_length=16, choices=BusinessUnitType.choices)
    ownership = models.CharField(
        max_length=16, choices=BusinessOwnership.choices, default=BusinessOwnership.OWNED
    )
    # Set when unit_type=CLINIC and the clinic is platform-managed. One
    # BusinessUnit per (owner, clinic) — enforced below.
    clinic = models.ForeignKey(
        "clinics.Clinic",
        on_delete=models.CASCADE,
        null=True,
        blank=True,
        related_name="business_units",
    )
    # For PARTNERED units: the agreed commission percentage. Informational
    # only in Phase 1 — no auto-computation; the doctor records the
    # commission amount they actually received as a manual RevenueEntry.
    commission_rate = models.DecimalField(max_digits=5, decimal_places=2, null=True, blank=True)
    contact_name = models.CharField(max_length=255, blank=True)
    contact_phone = models.CharField(max_length=15, blank=True)
    address = models.CharField(max_length=500, blank=True)
    notes = models.TextField(blank=True)
    is_active = models.BooleanField(default=True)

    class Meta(BaseModel.Meta):
        # Must subclass BaseModel.Meta — a bare `class Meta:` on a concrete
        # model silently drops the abstract base's `ordering` rather than
        # inheriting it (see apps.clinics.models.Clinic for the same note).
        constraints = [
            models.UniqueConstraint(
                fields=["owner", "clinic"],
                condition=models.Q(clinic__isnull=False),
                name="unique_owner_clinic_business_unit",
            )
        ]

    def __str__(self):
        return f"BusinessUnit<{self.owner_id}:{self.name}>"


# ---------------------------------------------------------------------------
# RevenueShareRule — who gets what percentage
# ---------------------------------------------------------------------------


class RevenueShareRule(BaseModel):
    """
    Per-doctor-per-clinic revenue split configuration, managed by the
    clinic OWNER only. When
    `enabled`, every new revenue entry generated by `doctor`'s work at
    `clinic` is split: the doctor's dashboard counts
    `doctor_share_percentage`% of the amount, the owner's dashboard
    counts the remainder. Applied as a SNAPSHOT at entry-capture time
    (see RevenueEntry.split_enabled/doctor_share_percentage) — editing or
    disabling a rule affects future entries only, never history.

    No rule (or `enabled=False`) means no split at all: the conducting
    doctor is credited the entire amount.
    """

    clinic = models.ForeignKey(
        "clinics.Clinic", on_delete=models.CASCADE, related_name="revenue_share_rules"
    )
    doctor = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.CASCADE, related_name="revenue_share_rules"
    )
    enabled = models.BooleanField(default=True)  # the "percentage boolean flag"
    doctor_share_percentage = models.DecimalField(max_digits=5, decimal_places=2)  # 0-100
    created_by = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name="+",
    )
    notes = models.TextField(blank=True)  # e.g. "60/40 as agreed July 2026"

    class Meta(BaseModel.Meta):
        constraints = [
            models.UniqueConstraint(
                fields=["clinic", "doctor"], name="unique_clinic_doctor_share_rule"
            ),
            models.CheckConstraint(
                check=models.Q(doctor_share_percentage__gte=0, doctor_share_percentage__lte=100),
                name="share_percentage_0_100",
            ),
        ]

    def __str__(self):
        return (
            f"RevenueShareRule<{self.clinic_id}:{self.doctor_id}={self.doctor_share_percentage}%>"
        )


