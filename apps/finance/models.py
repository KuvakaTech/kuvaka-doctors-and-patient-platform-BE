class FinanceGrantStatus(models.TextChoices):
    ACTIVE = "active", "Active"
    REVOKED = "revoked", "Revoked"
    EXPIRED = "expired", "Expired"


class FinanceAccessGrant(BaseModel):
    """
    A doctor granting another platform user read access to their finance
    data. View-only — no grant tier allows creating/editing entries on
    the doctor's behalf. Mirrors the apps.patients.ConsentGrant /
    apps.clinics.StaffTaskGrant lifecycle pattern already in the codebase.
    """

    doctor = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.CASCADE, related_name="finance_grants_given"
    )
    grantee = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        on_delete=models.CASCADE,
        related_name="finance_grants_received",
    )
    # Optional scope narrowing — both null = the doctor's ENTIRE finance
    # picture. Set one to limit the grantee to a single clinic's or a
    # single business unit's slice.
    clinic = models.ForeignKey(
        "clinics.Clinic", on_delete=models.CASCADE, null=True, blank=True, related_name="+"
    )
    business_unit = models.ForeignKey(
        BusinessUnit, on_delete=models.CASCADE, null=True, blank=True, related_name="+"
    )
    status = models.CharField(
        max_length=16, choices=FinanceGrantStatus.choices, default=FinanceGrantStatus.ACTIVE
    )
    expires_at = models.DateTimeField(null=True, blank=True)
    revoked_at = models.DateTimeField(null=True, blank=True)
    notes = models.TextField(blank=True)

    class Meta(BaseModel.Meta):
        constraints = [
            models.CheckConstraint(
                check=~models.Q(doctor=models.F("grantee")), name="finance_grant_not_self"
            ),
            # At most one scope narrowing.
            models.CheckConstraint(
                check=~models.Q(clinic__isnull=False, business_unit__isnull=False),
                name="finance_grant_single_scope",
            ),
        ]
        indexes = [models.Index(fields=["grantee", "status"])]

    def __str__(self):
        return f"FinanceAccessGrant<{self.doctor_id}->{self.grantee_id}:{self.status}>"
